export async function seedFoods() {
  try {
    console.log('ðŸŒ± Iniciando seed de alimentos...');

    // Limpiar alimentos existentes (opcional)
    console.log('ðŸ§¹ Limpiando alimentos existentes...');
    await prisma.mealIngredient.deleteMany();
    await prisma.recipeItem.deleteMany();
    await prisma.food.deleteMany();

    // Crear alimentos
    console.log('ðŸ¥• Creando alimentos...');
    const createdFoods = await Promise.all(
      foods.map(food =>
        prisma.food.upsert({
          where: { name: food.name },
          update: food,
          create: food
        })
      )
    );
    console.log(`âœ… ${createdFoods.length} alimentos creados`);

    // Actualizar alternativas (ahora que tenemos IDs)
    console.log('ðŸ”„ Actualizando alternativas entre alimentos...');

    // Ejemplo: establecer tofu como alternativa a la pechuga de pollo para vegetarianos
    const tofu = createdFoods.find((i: any) => i.name === 'Tofu');
    const chicken = createdFoods.find((i: any) => i.name === 'Pechuga de pollo');

    if (tofu && chicken) {
      await prisma.food.update({
        where: { id: chicken.id },
        data: {
          alternativesIds: [tofu.id]
        }
      });

      console.log('âœ… Alternativas actualizadas');
    }

    console.log('ðŸŽ‰ Seed de alimentos completado exitosamente!');
    return createdFoods;
  } catch (error: any) {
    console.error('âŒ Error durante el seed de alimentos:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

// Ejecutar el seed si este archivo se ejecuta directamente
if (require.main === module) {
  seedFoods()
    .then(() => {
      console.log('Seed de alimentos ejecutado exitosamente');
      process.exit(0);
    })
    .catch((error: any) => {
      console.error('Error ejecutando seed de alimentos:', error);
      process.exit(1);
    });
}